#!/usr/bin/env node
/**
 * Extracts all <style> blocks from the build-city-template.js templates
 * and writes a deduped, concatenated CSS file at public/css/city-template.css.
 *
 * The build-city-template.js generator should then strip <style> blocks from
 * the section HTML and inject <link rel="stylesheet" href="/css/city-template.css">
 * on the hero section only.
 */

const fs = require('fs');
const path = require('path');

const SRC = path.join(__dirname, 'build-city-template.js');
const AVIS = path.join(__dirname, 'snb-avis-block.html');
const OUT = path.join(__dirname, '..', 'public', 'css', 'city-template.css');

const sources = [fs.readFileSync(SRC, 'utf8'), fs.readFileSync(AVIS, 'utf8')];

// Extract every <style>...</style> block from the template literals.
const styleRe = /<style>([\s\S]*?)<\/style>/g;
const blocks = [];
for (const src of sources) {
  let m;
  while ((m = styleRe.exec(src))) {
    blocks.push(m[1].trim());
  }
  styleRe.lastIndex = 0;
}

// Dedupe identical blocks.
const seen = new Set();
const unique = blocks.filter(b => {
  if (seen.has(b)) return false;
  seen.add(b);
  return true;
});

console.log('Found', blocks.length, 'style blocks |', unique.length, 'unique');

// Concatenate.
const header = `/* Shared CSS for IDF city template — auto-generated by scripts/extract-city-css.js.\n   Do not edit by hand. Re-run the extractor after updating scripts/build-city-template.js. */\n\n`;
const css = header + unique.join('\n\n');

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, css, 'utf8');
console.log('Wrote', OUT, '(' + css.length + ' bytes)');
